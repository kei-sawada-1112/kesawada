#include <stdlib.h>

void	*ft_memcpy(void *dst, const void *src, size_t count)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (!dst && !src)
		return (NULL);
	while (count--)
		*d++ = *s++;
	return (dst);
}
