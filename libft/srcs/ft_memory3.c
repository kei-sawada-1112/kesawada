#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ustr;
	size_t			i;

	ustr = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		ustr[i] = '\0';
		i++;
	}
}
